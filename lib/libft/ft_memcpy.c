#include "libft.h"
#include <string.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char		*pdest;
	const char	*psrc;

	pdest = dest;
	psrc = src;
	if (!dest && !src)
		return (NULL);
	while (n--)
		*(pdest++) = *psrc++;
	return (dest);
}
